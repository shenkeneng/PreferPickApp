package com.frxs.wmspick.model;

public class PickingOrderCount {


    /**
     * UnFinishedCnt : 0
     * FinishedCnt : 0
     * TotalCnt : 0
     */

    private int UnFinishedCnt;
    private int FinishedCnt;
    private int TotalCnt;

    public int getUnFinishedCnt() {
        return UnFinishedCnt;
    }

    public void setUnFinishedCnt(int UnFinishedCnt) {
        this.UnFinishedCnt = UnFinishedCnt;
    }

    public int getFinishedCnt() {
        return FinishedCnt;
    }

    public void setFinishedCnt(int FinishedCnt) {
        this.FinishedCnt = FinishedCnt;
    }

    public int getTotalCnt() {
        return TotalCnt;
    }

    public void setTotalCnt(int TotalCnt) {
        this.TotalCnt = TotalCnt;
    }
}
